/**
 * Haversine 距離計算（km）
 * 2地点間の大圏距離を返す。
 */
export function haversineDistance(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number,
): number {
  const R = 6371; // 地球半径 km
  const toRad = (deg: number) => (deg * Math.PI) / 180;

  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

  return R * c;
}

/**
 * 住所の緯度経度から最寄り駅を検索
 *
 * @param lat  緯度
 * @param lng  経度
 * @param maxWalkMinutes 徒歩上限（分）。デフォルト10分
 * @returns 徒歩 maxWalkMinutes 以内の駅リスト（近い順）。
 *          該当なしの場合は最寄り1駅のみ返す。
 *
 * 計算前提:
 *   - 徒歩速度: 80 m/min（不動産表示基準）
 *   - 道路迂回率: 直線距離 x 1.3（都市部の平均的な補正係数）
 */
export function findNearestStations(
  lat: number,
  lng: number,
  maxWalkMinutes: number = 10,
): Array<{ station: string; distanceKm: number; walkMinutes: number }> {
  const WALK_SPEED_M_PER_MIN = 80; // m/min
  const ROAD_FACTOR = 1.3; // 直線→実距離補正

  const maxStraightKm = (maxWalkMinutes * WALK_SPEED_M_PER_MIN) / (ROAD_FACTOR * 1000);

  const results: Array<{ station: string; distanceKm: number; walkMinutes: number }> = [];

  for (const [station, coord] of Object.entries(STATION_COORDINATES)) {
    const straightKm = haversineDistance(lat, lng, coord.lat, coord.lng);
    const roadKm = straightKm * ROAD_FACTOR;
    const walkMin = Math.round((roadKm * 1000) / WALK_SPEED_M_PER_MIN);

    results.push({
      station,
      distanceKm: Math.round(roadKm * 100) / 100,
      walkMinutes: walkMin,
    });
  }

  // 距離でソート
  results.sort((a, b) => a.distanceKm - b.distanceKm);

  // 徒歩圏内の駅を抽出
  const withinWalk = results.filter(
    (r) => haversineDistance(lat, lng, STATION_COORDINATES[r.station].lat, STATION_COORDINATES[r.station].lng) <= maxStraightKm,
  );

  // 該当なしなら最寄り1駅のみ
  if (withinWalk.length === 0) {
    return results.slice(0, 1);
  }

  return withinWalk;
}
